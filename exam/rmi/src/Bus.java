public class Bus {
    public String driverName;
    public String model;
    public int route;
    public int age;
    public int mileage;

    public Bus(String driverName, String model, int route, int age, int mileage) {
        this.driverName = driverName;
        this.model = model;
        this.route = route;
        this.age = age;
        this.mileage = mileage;
    }
}
